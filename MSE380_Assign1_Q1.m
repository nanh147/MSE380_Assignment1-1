t = 0:0.0005:15;
t_mat = vec2mat(t,1);
b=20;
k=500;
k_theta = 1000;
b_theta = 80;
p=7874;
L=0.15;
r=0.75;
h=0.1;
m = p*L*L*L;
j = 0.5*m*r^2;
a = [b/m 0 1/m 0; b*r/j b_theta/j r/j 1/j; 1 0 0 0;0 1 0 0];
bf = [1/m;-r/j;0;0];
bg = [1/m;-r/j;0;0];
c = [0 0 1/k 0;0 0 0 1/k_theta];
d=0;
x = [0;0;0;0];
x_diff = [0;0;0;0];
y=[];
n=15/0.0005;
ft(1:1,1:n+1) =0;
ft(1,1) = 100;
i=1;
g(1:1,1:n+1) = 9.81;
ut = [ft;g];
for t = 0:0.0005:15
    x_diff = a*x(:,i) + bf.*ft(:,i) + bg.*g(:,i);
    x(:,i+1) = x(:,i) + x_diff.*dt;
    y(:,i) = c*x(:,i);
    i=i+1;
end
figure;
plot(t_mat,y(1,:),t_mat,y(2,:));
